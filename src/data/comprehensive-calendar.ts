// ==================== INTERFACES ====================

export type EventCategory = "academic" | "holiday" | "special" | "parent";
export type AcademicPeriod =
  | "PRIMER_SEMESTRE"
  | "SEGUNDO_SEMESTRE"
  | "VACACIONES"
  | "ANUAL";
export type GradeLevel = "NT1" | "NT2" | "both" | "all";
export type EventPriority = "LOW" | "MEDIUM" | "HIGH";

export interface AcademicEvent {
  id: string;
  title: string;
  description?: string;
  startDate: string; // YYYY-MM-DD format
  endDate: string; // YYYY-MM-DD format
  priority: EventPriority;
  isAllDay: boolean;
  period: AcademicPeriod;
  gradeLevel: GradeLevel;
  category: "academic";
}

export interface ChileanHoliday {
  id: string;
  name: string;
  description?: string;
  date: string; // YYYY-MM-DD format
  isNational: boolean;
  region?: string;
  isRecurring: boolean;
  category: "holiday";
}

export interface CalendarEvent {
  id: string;
  title: string;
  description?: string;
  date: string; // ISO format YYYY-MM-DD
  time?: string; // Optional time field for events
  location?: string; // Optional location field for events
  category: EventCategory;
  level?: "NT1" | "NT2" | "both"; // Niveles de transición
  isRecurring?: boolean;
  color?: string;
}

export type ComprehensiveCalendarEvent =
  | (AcademicEvent & {
      name?: string;
      date?: string;
      isNational?: boolean;
      isRecurring?: boolean;
      time?: string;
      location?: string;
      color?: string;
    })
  | (ChileanHoliday & {
      title?: string;
      startDate?: string;
      endDate?: string;
      priority?: EventPriority;
      period?: AcademicPeriod;
      gradeLevel?: GradeLevel;
      time?: string;
      location?: string;
      color?: string;
    })
  | (CalendarEvent & {
      name?: string;
      date?: string;
      isNational?: boolean;
      priority?: EventPriority;
      period?: AcademicPeriod;
      gradeLevel?: GradeLevel;
    });

